#Configurations
swapsize=4G
swapfile="/swap.img"
dbuser="ptdbuser"
dbpass="ptdbpass"
dbname="guacamole_db" 
numofPT=10
PTfile="PacketTracer_731_amd64.deb"
PTfilechecksum="172350fc0f8bc8da04e44017d9fab4850f3af871"
nginxport=80
#1G memory + 4G swap can support about 10 concurrent PT user
#DO NOT change dbname

